// In-memory follow state (replaced by DB)
let profiles = [...MOCK_PROFILES];

export function getMockProfile(userId: string): UserProfile | undefined {
  return profiles.find((p) => p.id === userId);
}

export function getFollowingIds(): string[] {
  return profiles.filter((p) => p.isFollowing).map((p) => p.id);
}

export function setFollowing(userId: string, following: boolean): void {
  profiles = profiles.map((p) => {
    if (p.id !== userId) return p;
    return {
      ...p,
      isFollowing: following,
      followersCount: following ? p.followersCount + 1 : Math.max(0, p.followersCount - 1),
    };
  });
}
